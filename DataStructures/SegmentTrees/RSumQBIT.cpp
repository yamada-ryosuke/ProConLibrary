// http://judge.u-aizu.ac.jp/onlinejudge/description.jsp?id=DSL_2_B&lang=jp
int main()
{
	int n, q;
	scanf("%d%d", &n, &q);
	RSumQBIT rsq(n);
	for (int i{}; i < q; i++)
	{
		int com, x, y;
		scanf("%d%d%d", &com, &x, &y);
		if (!com) rsq.update(x - 1, y);
		else printf("%lld\n", rsq.get(x - 1, y));
	}

	return 0;
}
