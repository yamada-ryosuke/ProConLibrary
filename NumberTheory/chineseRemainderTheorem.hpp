/////////////////

// quotient1とquotient2は互いに素
int64_t chineseRemainderTheorem(const int64_t quotient1, const int64_t remainder1, const int64_t quotient2, const int64_t remainder2)
{
	auto ext{extendedEuclidean(quotient1, quotient2)};
	int64_t ret{quotient1 * ext[0] * remainder2 + quotient2 * ext[1] * remainder1};
	if (ret < 0) ret += quotient1 * quotient2;
	return ret;
}